let day:string = prompt('Введіть день тижня:')

switch (day){
    case 'Понеділок':
        console.log('взлзилщилащвл хзлщалщ зіщмлщ');
        break;

    case 'Вівторок':
        console.log('щалащлі щліаошвщо воьшоаід');
        break;

    case 'Середа':
        console.log('алоашоіш вщлщів ілваівьаів');
        break;

    case 'Четвер':
        console.log('фвшіоі івділвдфлвдлф оаооао');
        break;

    case "П'ятниця":
        console.log('вдлщлщ філвліов ділвфдвлфі');
        break;

    case 'Субота':
        console.log('влащвлаів вщлщлаві алівщалвіщ');
        break;

    case 'Неділя':
        console.log('валщзівлащфла валщвла лвтвотсотіс');
        break;

    default:
        console.log('Неправильний день')
}
